package Links;

public class Holder {           //class used to hold variables that will be used for calculation
    public int a;
    public int b;
    public int c;
    public int j;


    public int getA(){
        return a;
    }

    public void setA(int a){
        this.a=a;
    }

    public int getB(){
        return b;
    }

    public void setB(int b){
        this.b=b;
    }

    public int getC(){
        return c;
    }
    public void setC(int c){
        this.c=c;
    }

    public int getJ(){
        return j;
    }
    public void setJ(int j){
        this.j=j;
    }
}


